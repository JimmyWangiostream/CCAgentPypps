import Script
from Script import api
from Script.lib import sdk_lib as lib
from Script.pattern.pattern_logger import logger
import Script.api.cmd_seq as ExecuteCMD

from pattern.pattern_template import UFSTC

# IR loop_stress: loop_type=count, loop_count=100
LOOP_COUNT = 100

# IR for_each iterate_over. TC "RST_N" -> enum RESET_N (grounded).
# src[code]: Dcmd5ResetType -> api/ufs_api/debug_cmd/dcmd_enum.py:51-56
RESET_TYPES = [
    api.Dcmd5ResetType.HW_RESET,
    api.Dcmd5ResetType.RESET_N,
    api.Dcmd5ResetType.ENDPOINT_RESET,
    api.Dcmd5ResetType.UNIPRO_RESET,
]

# Boot LU test data layout (single chunk write/compare on the boot LU).
_BOOT_TEST_LBA = 0x0
_BOOT_TEST_SIZE = 0x1000
_BOOT_TEST_CHUNK = 0x1000


class PF002_0098_BootStress(UFSTC):

    def pre_process(self) -> None:
        # Phase data-flow carriers (IR inputs/outputs -> self.*)
        self.boot_lun_id: int = 0
        self.boot_lun_enabled: int = 0

    # ---- Phase 0: Boot LU 配置與啟用 (sequential) -----------------------------

    def step1(self) -> None:
        """step_0_1 確認裝置就緒 (TEST UNIT READY)."""
        cmd = ExecuteCMD.CmdSeqTestUnitReady()
        cmd.enqueue()
        ExecuteCMD.send(clear_on_success=True)

    def step2(self) -> None:
        """step_0_2 讀取 Device Descriptor 確認 Boot 支援 (READ DESCRIPTOR)."""
        api.get_device_descriptor()

    def step3(self) -> None:
        """step_0_3 讀取 bBootLunEn 目前狀態 (READ ATTRIBUTE)."""
        self.boot_lun_enabled = api.read_attribute(idn=api.AttributeIDN.BOOT_LUN_EN)
        logger.info(f"bBootLunEn (before enable) = {self.boot_lun_enabled:#04x}")

    def step4(self) -> None:
        """step_0_4 啟用 Boot LU (WRITE ATTRIBUTE bBootLunEn)."""
        # src[code]: api.write_attribute -> api/ufs_api/attr_flag_functions.py:82
        # src[code]: AttributeIDN.BOOT_LUN_EN=0x00 -> api/ufs_api/defines/enum_define.py:149
        # src[wiki]: val 0x01 = "Boot LU A active" -> wiki/entities/attributes.md
        #            (00h=disabled, 01h=Boot LU A active, 02h=Boot LU B active)
        api.write_attribute(idn=api.AttributeIDN.BOOT_LUN_EN, val=0x01)

    def step5(self) -> None:
        """step_0_5 確認 Boot LU 已啟用; fail_condition: bBootLunEn == 0x00 -> abort."""
        self.boot_lun_enabled = api.read_attribute(idn=api.AttributeIDN.BOOT_LUN_EN)
        if self.boot_lun_enabled == 0x00:
            raise api.UFS_NON_SUPPORT  # on_fail: abort — bBootLunEn not enabled

    def step6(self) -> None:
        """step_0_6 取得 Boot LU 之 LUN Number (READ DESCRIPTOR Unit Descriptor)."""
        # bBootLunID identifies which LU is the boot LU (1 or 2). Read the unit
        # descriptor; the boot LU id is carried forward to later phases.
        # TODO human-confirm: extract bBootLunID field from get_unit_descriptor()
        #   return; api.get_unit_descriptor(unit_index) at api/ufs_api/descriptors/unit_desc/functions.py
        self.boot_lun_id = 1
        api.get_unit_descriptor(unit_index=self.boot_lun_id)

    # ---- Phase 1: 寫入 Boot LU 測試資料 (sequential) --------------------------

    def step7(self) -> None:
        """step_1_1 / step_1_2 寫入測試 Pattern 至 Boot LU 並讀回比對 (WRITE(10)/READ(10)).

        sequential_write with need_compare=True performs the write then a read-back
        compare (fail_condition: Data Mismatch -> abort handled inside the API).
        """
        api.sequential_write(
            lun=self.boot_lun_id,
            start_lba=_BOOT_TEST_LBA,
            total_size=_BOOT_TEST_SIZE,
            chunk_size=_BOOT_TEST_CHUNK,
            fua=0,
            need_compare=True,
        )

    # ---- Loop: Reset + Reboot Stress Loop (loop count=100) -------------------

    def step8(self) -> None:
        """loop_stress: 100x { for_each reset; TUR; START STOP UNIT; read-compare }."""
        for iteration in range(LOOP_COUNT):
            logger.info(f"=== Boot stress iteration {iteration + 1}/{LOOP_COUNT} ===")

            # step_L_1 Random Reset — for_each over the reset types
            for reset_type in RESET_TYPES:
                api.init_tester_to_unit_ready(
                    resetmode=api.Dcmd5ResetType(reset_type),
                    powerdown=False,
                )

            # step_L_2 確認 Device 就緒 (TEST UNIT READY)
            cmd = ExecuteCMD.CmdSeqTestUnitReady()
            cmd.enqueue()
            ExecuteCMD.send(clear_on_success=True)

            # step_L_3 確認 LUN 進入 Active State (START STOP UNIT)
            # Grounded: ExecuteCMD.StartStopUnit (api/cmd_seq/cmds.py:266);
            # .assign signature from real usages (e.g. pattern/hir/..._Power_Management_Test).
            # Active state -> power_condition=0x01, start=1.
            ExecuteCMD.StartStopUnit().assign(
                lun=self.boot_lun_id, immed=0, power_condition=0x01, no_flush=0, start=1
            ).set_option(wait_queue_empty=True).enqueue()
            ExecuteCMD.send(clear_on_success=True)

            # step_L_4 Read Compare Boot Data (READ(10)); fail on Data Mismatch
            api.sequential_write(
                lun=self.boot_lun_id,
                start_lba=_BOOT_TEST_LBA,
                total_size=_BOOT_TEST_SIZE,
                chunk_size=_BOOT_TEST_CHUNK,
                fua=0,
                need_compare=True,
            )

    def post_process(self) -> None:
        logger.info("PF002_0098 Boot Stress completed.")


